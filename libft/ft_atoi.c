#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	int res;
	int negative;

	i = 0;
	res = 0;
	negative = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = 10 * res + str[i] - '0';
		i++;
	}
	if (negative == 1)
		res = -res;
	return (res);
}
