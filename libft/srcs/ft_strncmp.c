#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	if (len == 0)
		return (0);
	while (--len && (t_uc) * s1 == (t_uc) * s2 && *s1 && *s2)
	{
		++s1;
		++s2;
	}
	return ((t_uc) * s1 - (t_uc) * s2);
}
