#include "libft.h"

void		*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	k;

	k = 0;
	while (k < n)
	{
		((char*)dst)[k] = ((char*)src)[k];
		k++;
	}
	return (dst);
}
